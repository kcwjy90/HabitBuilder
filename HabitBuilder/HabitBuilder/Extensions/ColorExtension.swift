import UIKit

extension UIColor {
    @nonobjc class var exoticLiras: UIColor { return UIColor(red:0.87, green:0.16, blue: 0.16, alpha: 1.00) }
    @nonobjc class var starWhite: UIColor { return UIColor(red:0.94, green:0.94, blue: 0.96, alpha: 1.00) }
    @nonobjc class var baseBrown: UIColor { return UIColor(red:247/255, green:237/255, blue: 219/255, alpha: 1.00) }
    @nonobjc class var todayGreen: UIColor { return UIColor(red:204/255, green:214/255, blue: 166/255, alpha: 1.00) }
    @nonobjc class var weekGreen: UIColor { return UIColor(red:218/255, green:226/255, blue: 182/255, alpha: 1.00) }
    @nonobjc class var restGreen: UIColor { return UIColor(red:223/255, green:232/255, blue: 204/255, alpha: 1.00) }

}


